let number = Number(prompt("Кез келген санды енгізіңіз:"));

for (let i = 1; i <= number; i++) {
    if (i < 20 || i > 30) {
        continue; // Пропускаем числа, не находящиеся в диапазоне 20-30
    }
    if (i % 2 === 0) {
        alert(i); // Выводим только четные числа в диапазоне 20-30
    }
}
